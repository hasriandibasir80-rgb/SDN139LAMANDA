/**
 * Load Data dari Firebase
 */
async function loadDataSiswa() {
  try {
    const snapshot = await get(ref(database, 'siswa'));
    allSiswaData = [];
    let totalL = 0, totalP = 0;
    const kelasSet = new Set();

    if (snapshot.exists()) {
      snapshot.forEach(kelasSnap => {
        const kelas = kelasSnap.key;
        kelasSet.add(kelas);
        
        kelasSnap.forEach(siswaSnap => {
          const data = siswaSnap.val();
          allSiswaData.push({
            id: siswaSnap.key,
            kelas: kelas,
            nisn: data.nisn || '-',
            nama: data.nama || 'Tanpa Nama',
            jk: data.jenisKelamin || data.jk || '-',
            tglLahir: data.tanggalLahir || data.tglLahir || '-',
            alamat: data.alamat || '-',
            ortu: data.ortu || data.orangTua || '-'
          });

          if (data.jenisKelamin === 'L' || data.jk === 'L') totalL++;
          else if (data.jenisKelamin === 'P' || data.jk === 'P') totalP++;
        });
      });
    }

    // Sort by Nama
    allSiswaData.sort((a, b) => a.nama.localeCompare(b.nama));
    filteredData = [...allSiswaData];

    // Update Stats
    document.getElementById('statTotal').textContent = allSiswaData.length;
    document.getElementById('statKelas').textContent = kelasSet.size;
    document.getElementById('statL').textContent = totalL;
    document.getElementById('statP').textContent = totalP;

    // Render Table & Chart
    renderTable();
    renderChart(totalL, totalP); // Placeholder chart
    loadPrestasiDummy();

  } catch (error) {
    console.error('Error loading data:', error);
    document.getElementById('tabelBody').innerHTML = 
      `<tr><td colspan="8" style="text-align:center; color:red;">Gagal memuat data: ${error.message}</td></tr>`;
  }
}

/**
 * Render Tabel
 */
function renderTable() {
  const tbody = document.getElementById('tabelBody');
  tbody.innerHTML = '';

  if (filteredData.length === 0) {
    tbody.innerHTML = '<tr><td colspan="8" style="text-align:center;">Tidak ada data ditemukan</td></tr>';
    return;
  }

  filteredData.forEach((siswa, index) => {
    const tr = document.createElement('tr');
    tr.innerHTML = `
      <td>${index + 1}</td>
      <td>${siswa.nisn}</td>
      <td><strong>${siswa.nama}</strong></td>
      <td>${siswa.kelas}</td>
      <td>${siswa.jk === 'L' ? 'Laki-laki' : 'Perempuan'}</td>
      <td>${siswa.tglLahir}</td>
      <td>${siswa.alamat}</td>
      <td>${siswa.ortu}</td>
    `;
    tbody.appendChild(tr);
  });
}

/**
 * Render Chart Kehadiran (CSS Conic Gradient)
 */
function renderChart(l, p) {
  // Dummy data kehadiran untuk visualisasi
  const hadir = 85;
  const izin = 8;
  const sakit = 5;
  const alfa = 2;
  const total = hadir + izin + sakit + alfa;

  // Calculate degrees for conic-gradient
  const hDeg = (hadir / total) * 360;
  const iDeg = hDeg + (izin / total) * 360;
  const sDeg = iDeg + (sakit / total) * 360;

  const chart = document.getElementById('pieChart');
  chart.style.background = `conic-gradient(
    #3b82f6 0deg ${hDeg}deg, 
    #fbbf24 ${hDeg}deg ${iDeg}deg, 
    #f97316 ${iDeg}deg ${sDeg}deg, 
    #ef4444 ${sDeg}deg 360deg
  )`;

  document.getElementById('chartCenterText').textContent = `${hadir}%`;

  const legend = document.getElementById('chartLegend');
  legend.innerHTML = `
    <div class="legend-item"><span class="legend-color" style="background:#3b82f6"></span> Hadir</div>
    <div class="legend-item"><span class="legend-color" style="background:#fbbf24"></span> Izin</div>
    <div class="legend-item"><span class="legend-color" style="background:#f97316"></span> Sakit</div>
    <div class="legend-item"><span class="legend-color" style="background:#ef4444"></span> Alfa</div>
  `;
}

